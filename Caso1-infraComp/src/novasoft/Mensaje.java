package novasoft;

public class Mensaje {
	
	private int contenido;
	private int respuesta;
	
	public int getContenido() {
		return contenido;
	}	
	public void setContenido(int pCont) {
		contenido = pCont;
	}	
	public int getRespuesta()
	{
		return respuesta;
	}	
	public void setRespuesta(int resp)
	{
		respuesta=resp;
	}
	
}
